var number = 10;
var number2 = 5;
var numberFloat = 0.45;

// 각 변수 값 출력
console.log("변수 값 출력");
console.log(number);
console.log(number2);
console.log(numberFloat);

// 덧셈 연산자
console.log("덧셈 +");
console.log(number + numberFloat);

// 뺄셈 연산자
console.log("뺄셈 -");
console.log(number - numberFloat);

// 곱셈 연산자
console.log("곱셈 *");
console.log(number * number2);

// 나눗셈 연산자
console.log("나눗셈 /");
console.log(number / number2);

// 나머지 연산자
console.log("나머지 %");
console.log(number2 % number);

// 각 변수 값 출력
console.log("변수 값 출력");
console.log(number);
console.log(number2);
console.log(numberFloat);

// 복합 연산
console.log("복합 연산 += | -=");
console.log(numberFloat += 5);
console.log(numberFloat);

// 단항 연산
console.log("단항 연산 ++ | --");
console.log(++number);
console.log(number);